/** 
 * Arithmetic Operators
 * Addition (+)
 * Subtraction (-)
 * Multiplication (*)
 * Division (/)
 * Modulus (%)
 * Exponentiation (**)
 */
let a = 5;
let b = 2;

console.log("a =", a, "& b =", b);
console.log("a + b =", a + b);
console.log("a - b =", a - b);
console.log("a * b =", a * b);
console.log("a / b =", a / b);
console.log("a % b =", a % b);
console.log("a ** b =", a ** b);

// Unary Operators
/**
 * Pre-Increment 
 * Changes in line
 */
console.log("++a =", ++a);

/**
 * Post Increment (changes in next line)
 * Changes in next line
 */
console.log("a++ =", a++);

/**
 * Pre-Decrement (changes in line)
 * Changes in line
 */
console.log("--b =", --b);

/**
 * Post Decrement
 * Changes in next line
 */
console.log("b-- =", b--);

/**
 * Assignment Operators (assign values)
 * Assignment (=)
 * Addition assignment (+=)
 * Subtraction assignment (-=)
 * Multiplication assignment (*=)
 * Modulus assignment (%=)
 * Exponentiation assignment (**=)
 * 
 * We can you all the arithmetic operators
 * in assignment operator's form
 */
let c = 5;
let d = 2;

// Assignment 
console.log("c =", c);

// Addition assignment
console.log("c +=2, c =", c += 2);

// Subtraction assignment
console.log("c -=2, c =", c -= 2);

// Multiplication assignment
console.log("c *= 2, c =", c *= 2);

// Modulus assignment
console.log("c %= 2, c =", c %= 2);

// Exponentiation assignment
console.log("d **= 2, d =", d **= 2);